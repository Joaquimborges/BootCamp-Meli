package com.company.exercicios.dakar;

public class Motos {

    private String marca;
    private String cor;


    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public Motos() {
    }

    public Motos(String marca, String cor) {
        this.marca = marca;
        this.cor = cor;
    }










}
